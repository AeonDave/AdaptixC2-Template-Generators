// DllToShellcode converts a Windows x64 PE DLL into position-independent
// shellcode using sRDI (shellcode Reflective DLL Injection).
//
// The output layout is:
//
//	[bootstrap 20B] [reflective loader 880B] [raw DLL bytes]
//
// When executed, the bootstrap locates the DLL in memory and calls the
// reflective loader, which performs in-memory PE loading (allocate, copy
// sections, relocate, resolve imports, call DllMain).
func DllToShellcode(dll []byte) ([]byte, error) {
	if len(dll) < 64 {
		return nil, fmt.Errorf("sRDI: input too small (%d bytes)", len(dll))
	}
	if dll[0] != 'M' || dll[1] != 'Z' {
		return nil, fmt.Errorf("sRDI: invalid PE (missing MZ header)")
	}

	// Patch bootstrap: lea rcx, [rax + offset_to_dll]
	// rax = addr of byte 5 (pop rax), DLL starts at srdiCodeSize,
	// so offset = srdiCodeSize - 5 = 895.
	boot := srdiBootstrap
	dllOffset := uint32(srdiCodeSize - 5) // 895
	binary.LittleEndian.PutUint32(boot[9:13], dllOffset)

	shellcode := make([]byte, 0, srdiCodeSize+len(dll))
	shellcode = append(shellcode, boot[:]...)
	shellcode = append(shellcode, srdiLoader[:]...)
	shellcode = append(shellcode, dll...)
	return shellcode, nil
}
